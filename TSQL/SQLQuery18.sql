
SELECT E.ENAME, E.EMPNO,D.LOC,D.DEPTNO,D.LOC
FROM EMP E
JOIN
DEPT D
ON(E.DEPTNO = D.DEPTNO)
AND D.LOC = 'CHICAGO';

SELECT E.ENAME,E.EMPNO,D.LOC,D.DEPTNO
FROM EMP E
JOIN DEPT D
ON(E.DEPTNO=D.DEPTNO)
ORDER BY LOC;

SELECT E.ENAME,D.LOC
FROM EMP E
JOIN
DEPT D
ON(D.DEPTNO=E.DEPTNO)
ORDER BY LOC;

SELECT E.ENAME AS 'MANAGER',C.ENAME AS 'EMPLOYEE'
FROM EMP  C
JOIN
EMP E
ON(E.EMPNO=C.MGR)
AND E.HIREDATE>C.HIREDATE;

SELECT E.ENAME,D.DNAME
FROM EMP E
JOIN
DEPT D
ON(E.DEPTNO=D.DEPTNO);

SELECT E.EMPNO, E.ENAME,E.HIREDATE,J.STARTDATE,J.ENDDATE
FROM EMP E
JOIN JOBHIST J
ON E.HIREDATE BETWEEN J.STARTDATE AND ENDDATE;

SELECT E.ENAME,E.EMPNO,D.LOC,D.DEPTNO
