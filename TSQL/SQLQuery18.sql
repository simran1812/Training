SELECT Manger.ENAME AS 'MANAGER', employee.ENAME AS 'EMPLOYEE'
FROM  EMP Manger FULL OUTER JOIN EMP employee
ON (Manger.EMPNO = employee.MGR)
where Manger.HIREDATE < employee.HIREDATE;

